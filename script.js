// creating operators functions


function add(a, b){
    return a + b;
}

function subtract (a, b){
    return a - b;
}

function multiply(a, b){
    return a * b;
}

function divide(a, b){
    return a / b;
}

console.log(add(3, 5));
console.log(subtract(9, 1));
console.log(multiply(4, 2));
console.log(divide(16, 2));